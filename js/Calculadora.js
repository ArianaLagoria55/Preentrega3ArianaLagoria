class Calculadora {
    //Funcion para sumar
    sumar(num1, num2) {
        return num1 + num2;
    }
    //Funcion para restar
    restar(num1, num2) {
        return num1 - num2;
    }
    //Funcion para dividir
    dividir(num1, num2) {
        return num1 / num2;
    }
    //Funcion para multiplicar
    multiplicar(num1, num2) {
        return num1 * num2;
    }
} 